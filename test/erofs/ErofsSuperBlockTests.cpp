TEST(TestErofsSuperBlock, basicParsing)
{
    uint8_t block[4096] = {};

    {
        auto *superPtr = reinterpret_cast<ErofsSuperBlockStruct *>(block + EROFS_SUPER_OFFSET);
        superPtr->magic = EROFS_MAGIC;
        superPtr->featureCompat = ErofsCompatFeature::ModificationTime;
        superPtr->blockSizeBits = 12;
        superPtr->extensionSlots = 0;
        superPtr->rootInode = 123;
        superPtr->inodeCount = 234;
        superPtr->buildTimeSec = 1768325570;
        superPtr->buildTimeNSec = 1234;
        superPtr->blocks = 56;
        superPtr->metaBlockAddr = 12;
        superPtr->xattrBlockAddr = 2;
        superPtr->featureIncompat = ErofsIncompatFeature::ZeroPadding;
    }

    auto sb = ErofsSuperBlock::parse(block, sizeof(block));
    ASSERT_TRUE(sb.has_value());

    EXPECT_EQ(sb->rootInode(), ino_t(123));
    EXPECT_EQ(sb->blockSize(), size_t(4096));
    EXPECT_EQ(sb->inodeOffset(9876), size_t((12 * 4096) + (9876 * 32)));

    EXPECT_TRUE(sb->hasFeature(ErofsSuperBlock::Feature::ZeroPadding));
    EXPECT_FALSE(sb->hasFeature(ErofsSuperBlock::Feature::SuperBlockChecksum));
    EXPECT_TRUE(sb->hasFeature(ErofsSuperBlock::Feature::ModificationTime));
    EXPECT_FALSE(sb->hasFeature(ErofsSuperBlock::Feature::XattrFilter));

    EXPECT_EQ(sb->buildTime().tv_sec, 1768325570);
    EXPECT_EQ(sb->buildTime().tv_nsec, 1234);
}

