TEST(TestErofsSuperBlock, superBlockChecksum)
{
    uint8_t block[4096];

    // Read data/empty.erofs.im which was generated by the mkfs.erofs tool
    {
        int fd = open(TEST_DATA_DIR "/erofs_1.img", O_CLOEXEC | O_RDONLY);
        ASSERT_GE(fd, 0);

        ssize_t rd = TEMP_FAILURE_RETRY(read(fd, block, sizeof(block)));
        ASSERT_EQ(rd, static_cast<ssize_t>(sizeof(block)));

        close(fd);
    }

    // Parse the superblock and ensure the checksum crc was set
    auto sb = ErofsSuperBlock::parse(block, sizeof(block));
    ASSERT_FALSE(sb.isError()) << "failed to parse superblock: " << sb.error().what();

    EXPECT_TRUE(sb->hasFeature(ErofsSuperBlock::Feature::ZeroPadding));
    EXPECT_TRUE(sb->hasFeature(ErofsSuperBlock::Feature::SuperBlockChecksum));
    EXPECT_TRUE(sb->hasFeature(ErofsSuperBlock::Feature::ModificationTime));
    EXPECT_FALSE(sb->hasFeature(ErofsSuperBlock::Feature::XattrFilter));

    // Corrupt a random byte and check if fails verification
    std::random_device rd;
    block[EROFS_SUPER_OFFSET + (rd() % 512)] ^= 0x40;

    sb = ErofsSuperBlock::parse(block, sizeof(block));
    ASSERT_FALSE(sb.has_value());
}
